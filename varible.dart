void main() {
  //int is Integer type
  int a = 24;
  //double is Decimal type
  double pie = 3.14;
  // String is word or multi word
  String name = "my name";
  //In var we can store any thing
  var n = "anything";
  var y = 23;
  print("int ${a}");
  print("double ${pie}");
  print("string ${name}");
  print("var ${n} and ${y}");
}
